package com.xwiki.projectmanagement.calendar;

public enum WeekDays
{
    SUNDAY(0),
    MONDAY(1),
    TUESDAY(2),
    WEDNESDAY(3),
    THURSDAY(4),
    FRIDAY(5),
    SATURDAY(6);

    private final int dayValue;

    private WeekDays(int dayValue)
    {
        this.dayValue = dayValue;
    }

    public int getDayValue()
    {
        return this.dayValue;
    }
}
